package Java_Basic.While_Loop_underStand;

public class Loop {
    static void main(String[] args) {

        for (int i = 0; i <5 ; i++) {
            System.out.println(i);
        }

        int i = 0;
        while (i<5){
            System.out.print(i);
            i++;
        }



    }
}

